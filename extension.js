// Neon Terminal 2030 — companion extension
// Adds a neon settings panel that wires up recommended editor settings,
// theme variants, and the optional CSS glow add-on.
const vscode = require("vscode");
const fs = require("fs");
const path = require("path");

const CUSTOM_CSS_EXT = "be5invis.vscode-custom-css";

const VARIANTS = [
  { id: "Neon Terminal 2030", label: "Neon Terminal 2030", swatch: "#0A0D14" },
  { id: "Neon Terminal 2030 (Deep Void)", label: "Deep Void (OLED)", swatch: "#000000" },
  { id: "Neon Terminal 2030 (Daylight)", label: "Daylight (Light)", swatch: "#FBFCFE" },
];

const ACCENTS = [
  { id: "mint", label: "Mint", color: "#34F5C5" },
  { id: "magenta", label: "Magenta", color: "#FF4D9D" },
  { id: "sky", label: "Sky", color: "#5BC0FF" },
  { id: "violet", label: "Violet", color: "#C792FF" },
  { id: "amber", label: "Amber", color: "#FFB84D" },
];

const RECOMMENDED = {
  "editor.bracketPairColorization.enabled": true,
  "editor.guides.bracketPairs": "active",
  "editor.semanticHighlighting.enabled": true,
  "editor.fontLigatures": true,
  "editor.stickyScroll.enabled": true,
  "editor.inlayHints.enabled": "onUnlessPressed",
  "editor.cursorBlinking": "phase",
  "editor.cursorSmoothCaretAnimation": "on",
  "editor.smoothScrolling": true,
};

/** Build the glow stylesheet from the user's chosen knobs. */
function generateGlowCss({ strength, radius, accent }) {
  const accentHex = (ACCENTS.find((a) => a.id === accent) || ACCENTS[0]).color;
  return `/* Generated by Neon Terminal 2030 settings panel — do not edit by hand. */
:root {
  --neon-glow-strength: ${strength};
  --neon-glow-radius: ${radius}px;
  --neon-accent: ${accentHex};
  --neon-magenta: #FF4D9D;
}
.monaco-editor .view-line span {
  text-shadow: 0 0 calc(var(--neon-glow-radius) * var(--neon-glow-strength)) currentColor;
}
.monaco-editor .mtki { text-shadow: none; }
.monaco-editor .cursor {
  box-shadow: 0 0 4px var(--neon-accent), 0 0 12px var(--neon-accent), 0 0 20px var(--neon-accent);
  animation: neon-cursor-pulse 1.4s ease-in-out infinite;
}
@keyframes neon-cursor-pulse { 0%,100%{opacity:1;} 50%{opacity:.45;} }
.monaco-editor .line-numbers.active-line-number { text-shadow: 0 0 8px var(--neon-accent); }
.monaco-workbench .activitybar .active-item-indicator::before {
  box-shadow: 0 0 8px var(--neon-accent), 0 0 16px var(--neon-accent);
}
.monaco-workbench .statusbar .statusbar-item.has-background-color { box-shadow: 0 0 10px var(--neon-magenta); }
.monaco-workbench .tab.active { box-shadow: inset 0 -2px 10px -4px var(--neon-accent); }
.monaco-list .monaco-list-row.focused { box-shadow: inset 2px 0 0 0 var(--neon-accent); }
.monaco-editor .suggest-widget, .monaco-editor .monaco-hover { box-shadow: 0 0 24px -8px var(--neon-accent); }
.monaco-editor .findMatch { box-shadow: 0 0 8px -2px #FFB84D; }
`;
}

function glowCssPath(context) {
  return path.join(context.globalStorageUri.fsPath, "neon-glow.generated.css");
}

async function applyRecommended() {
  const cfg = vscode.workspace.getConfiguration();
  for (const [key, value] of Object.entries(RECOMMENDED)) {
    await cfg.update(key, value, vscode.ConfigurationTarget.Global);
  }
  vscode.window.showInformationMessage("Neon Terminal 2030: recommended editor settings applied.");
}

async function applyVariant(themeId) {
  await vscode.workspace
    .getConfiguration()
    .update("workbench.colorTheme", themeId, vscode.ConfigurationTarget.Global);
}

async function enableGlow(context, opts) {
  const customCss = vscode.extensions.getExtension(CUSTOM_CSS_EXT);
  if (!customCss) {
    const pick = await vscode.window.showWarningMessage(
      'Glow needs the "Custom CSS and JS Loader" extension. Install it now?',
      "Install",
      "Cancel"
    );
    if (pick === "Install") {
      await vscode.commands.executeCommand("workbench.extensions.installExtension", CUSTOM_CSS_EXT);
    }
    return false;
  }

  // Write the tuned stylesheet into global storage.
  fs.mkdirSync(context.globalStorageUri.fsPath, { recursive: true });
  const cssPath = glowCssPath(context);
  fs.writeFileSync(cssPath, generateGlowCss(opts), "utf8");

  // Point Custom CSS at it (preserve any existing imports).
  const cfg = vscode.workspace.getConfiguration();
  const importUri = vscode.Uri.file(cssPath).toString();
  const current = cfg.get("vscode_custom_css.imports") || [];
  const others = current.filter((u) => !u.includes("neon-glow"));
  await cfg.update(
    "vscode_custom_css.imports",
    [...others, importUri],
    vscode.ConfigurationTarget.Global
  );

  const pick = await vscode.window.showInformationMessage(
    "Neon glow written. Run “Reload Custom CSS and JS”, then restart VS Code to see it.",
    "Reload Now"
  );
  if (pick === "Reload Now") {
    await vscode.commands.executeCommand("extension.updateCustomCSS");
  }
  return true;
}

async function disableGlow(context) {
  const cfg = vscode.workspace.getConfiguration();
  const current = cfg.get("vscode_custom_css.imports") || [];
  await cfg.update(
    "vscode_custom_css.imports",
    current.filter((u) => !u.includes("neon-glow")),
    vscode.ConfigurationTarget.Global
  );
  const cssPath = glowCssPath(context);
  if (fs.existsSync(cssPath)) fs.rmSync(cssPath);
  const pick = await vscode.window.showInformationMessage(
    "Neon glow disabled. Reload Custom CSS and restart to fully remove it.",
    "Reload Now"
  );
  if (pick === "Reload Now") {
    await vscode.commands.executeCommand("extension.updateCustomCSS");
  }
}

function currentState(context) {
  const cfg = vscode.workspace.getConfiguration();
  const imports = cfg.get("vscode_custom_css.imports") || [];
  return {
    theme: cfg.get("workbench.colorTheme"),
    glowOn: imports.some((u) => u.includes("neon-glow")),
    customCssInstalled: !!vscode.extensions.getExtension(CUSTOM_CSS_EXT),
    variants: VARIANTS,
    accents: ACCENTS,
  };
}

