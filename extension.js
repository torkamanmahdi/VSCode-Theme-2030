function openPanel(context) {
  const panel = vscode.window.createWebviewPanel(
    "neonTerminal2030.settings",
    "Neon Terminal 2030",
    vscode.ViewColumn.Active,
    { enableScripts: true, retainContextWhenHidden: true }
  );
  panel.webview.html = getWebviewHtml(currentState(context));

  panel.webview.onDidReceiveMessage(async (msg) => {
    switch (msg.type) {
      case "variant":
        await applyVariant(msg.id);
        break;
      case "recommended":
        await applyRecommended();
        break;
      case "glowOn":
        await enableGlow(context, { strength: msg.strength, radius: msg.radius, accent: msg.accent });
        break;
      case "glowOff":
        await disableGlow(context);
        break;
    }
  });
}

function activate(context) {
  context.subscriptions.push(
    vscode.commands.registerCommand("neonTerminal2030.openSettings", () => openPanel(context)),
    vscode.commands.registerCommand("neonTerminal2030.applyRecommended", () => applyRecommended()),
    vscode.commands.registerCommand("neonTerminal2030.toggleGlow", async () => {
      const on = (vscode.workspace.getConfiguration().get("vscode_custom_css.imports") || []).some(
        (u) => u.includes("neon-glow")
      );
      if (on) await disableGlow(context);
      else await enableGlow(context, { strength: 0.55, radius: 6, accent: "mint" });
    })
  );
}

function deactivate() {}

module.exports = { activate, deactivate };
